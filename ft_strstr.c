char	*ft_strstr(char *str, char *to_find)
{
	int x;
	int y;

	x = 0;
	while (str[x] != '\0')
	{
		y = 0;
		while (str[x + y] == to_find[y])
		{
			y++;
			if (to_find[y] == '\0')
				return (str + x);
		}
		x++;
	}
	return (0);
}
