#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t n)
{
	char			*s1;
	char			*s2;
	unsigned int	i;

	s1 = (char*)str1;
	s2 = (char*)str2;
	i = 0;
	while (s1[i] == s2[i] && ((s1[i] && s2[i]) != '\0') && (i < n - 1))
	{
		if (s1[i] != '\0' && (i < n - 1))
			i++;
	}
	return (s1[i] - s2[i]);
}
