#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int	x;

	x = 0;
	if (s && fd)
	{
		while (s[x] != '\0')
		{
			ft_putchar_fd(s[x], fd);
			x++;
		}
		ft_putchar_fd('\n', fd);
	}
}
