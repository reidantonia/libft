int	ft_strequ(char const *s1, char const *s2)
{
	int i;
	int result;

	i = 0;
	while (s1[i] == s2[i] && ((s1[i] && s2[i]) != '\0'))
	{
		if (s1[i] != '\0')
			i++;
	}
	result = s1[i] - s2[i];
	if (result == 0)
		return (1);
	return (0);
}
