#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	char	*s;

	s = (char*)str;
	while (*s++)
	{
		if (*s == c)
			return (s);
	}
	if (*s == c)
		return (s);
	else
		return (NULL);
}
