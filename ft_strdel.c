#include "libft.h"

void	ft_strdel(char **s)
{
	int i;

	i = 0;
	if (s && *s)
	{
		while (s[i] != '\0')
		{
			free(s[i]);
			i++;
		}
		free(s);
		*s = NULL;
	}
}
